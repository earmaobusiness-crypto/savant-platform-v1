-- Additive Purgatory Repair Bay durability (Track B demoted strategies).
-- Run manually in Supabase SQL editor when ready.

CREATE TABLE IF NOT EXISTS strategy_repair_profiles (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_key text UNIQUE NOT NULL,
    parent_layout_id text NOT NULL,
    strategy_label text NOT NULL,
    timeframe_resolution text NOT NULL,
    profile_json jsonb NOT NULL DEFAULT '{}'::jsonb,
    live_execution_enabled boolean NOT NULL DEFAULT false,
    demoted_at timestamptz NOT NULL DEFAULT now(),
    expires_at timestamptz NOT NULL,
    reminted_at timestamptz,
    created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_strategy_repair_parent_layout
    ON strategy_repair_profiles (parent_layout_id);

CREATE INDEX IF NOT EXISTS idx_strategy_repair_expires
    ON strategy_repair_profiles (expires_at)
    WHERE reminted_at IS NULL;
